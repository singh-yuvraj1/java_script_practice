// Arithmetic operators are used to perform arithmetic operations on operands.
// The arithmetic operators are:

`1. Addition (+)
 2. Subtraction (-)
 3. Multiplication (*)
 4. Division (/)
 5. Modulus (%)`


 //Exmample of arithmetic operators
// 1. Addition(+)
 let a = 5;
 let b = 10;
 console.log(a+b);
 

// 2. Subtraction(-)
 let c = 15;
 let d = 5;
 console.log(c-d);

// 3. Multiplication(*)
let e = 5;
let f = 10;
console.log(e*f);

// 4. Division(/)
let g = 20;
let h = 5;
console.log(g/h);   

// 5. Modulus(%)
let i = 10;
let j = 3;
console.log(i%j);   // 1 because modulus operator returns the remainder of the division of i by j. so when we divide 10 by 3 we get 3 as quotient and 1 as remainder. so modulus operator returns 1.