//Assignment operaotrs are used to assign values to variables. They are represented by the equal sign (=).

// Example
// 1. Simple assignment operator (=)
let a = 10;


// 2. Addition assignment operator (+=)
let b = 5;
b += 10;   // b = b + 10
console.log(b);   // 15 because addition assignment operator adds the value on the right side of the operator to the variable on the left side of the operator and assigns the result to the variable on the left side of the operator. so when we add 10 to b we get 15 and assign it to b.


// 3. Subtraction assignment operator (-=)
let c = 20; 
c -= 5;   // c = c - 5
console.log(c);   // 15 because subtraction assignment operator subtracts the value on the right side of the operator from the variable on the left side of the operator and assigns the result to the variable on the left side of the operator. so when we subtract 5 from c we get 15 and assign it to c.

//4. Multiplication assignment operator (*=)
let d = 5;
d *= 10;   // d = d * 10
console.log(d);   // 50 because multiplication assignment operator multiplies the variable on the left side of the operator by the value on the right side of the operator and assigns the result to the variable on the left side of the operator. so when we multiply d by 10 we get 50 and assign it to d.

//5. Division assignment operator (/=)
let e = 20;
e /= 5;   // e = e / 5
console.log(e);   // 4 because division assignment operator divides the variable on the left side of the operator by the value on the right side of the operator and assigns the result to the variable on the left side of the operator. so when we divide e by 5 we get 4 and assign it to e. 
