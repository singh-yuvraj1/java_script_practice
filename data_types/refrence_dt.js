// main difference between primitive and reference data types is that when we assign a primitive value to a variable,
//  it creates a copy of that value. So if we change the value of one variable, it does not affect the other variable. 
// However, when we assign a reference value to a variable, it creates a reference to the same object in memory.
//  So if we change the value of one variable, it will affect the other variable as well because they both 
// point to the same object in memory.